def main():
    rows = list(csv.DictReader(open(SRC, encoding="utf-8")))
    unmapped = set()
    for r in rows:
        tk = r["stock_ticker"]
        if tk == "_OTHER":
            r["sector"] = ""
            continue
        if tk not in SECTOR:
            unmapped.add(f'{tk} {r["stock_name"]}')
            continue
        r["sector"] = SECTOR[tk]
    if unmapped:
        raise SystemExit("未分類個股：\n" + "\n".join(sorted(unmapped)))

    with open(SRC, "w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=["etf_ticker", "stock_ticker", "stock_name",
                                          "sector", "weight_pct", "date"])
        w.writeheader()
        w.writerows(rows)
    sectors = sorted({v for v in SECTOR.values()})
    print(f"✅ 回填完成：{len(rows)} 列、{len(SECTOR)} 檔個股、{len(sectors)} 個細產業")
    print("、".join(sectors))


if __name__ == "__main__":
    main()
